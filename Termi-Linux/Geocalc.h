double opsgTrijustr(double x, double y, double z)
{
    return(x + y + z);
}

double opsgDvijustr(double x, double y)
{
    return(x + x + y + y);
}

double opsgKvdjustr(double x, int sqe)
{
    return(x*sqe);
}

double povrsDvijustr(double x, double y)
{
    return(x*y);
}

double povrsKvdjustr(double x)
{
    return(x*x);
}
